const setText = (selector, value, index = 0) => {
  const elements = document.querySelectorAll(selector);
  if (elements[index]) elements[index].textContent = value;
};

const setTexts = (selector, values) => {
  document.querySelectorAll(selector).forEach((element, index) => {
    if (values[index] !== undefined) element.textContent = values[index];
  });
};

const setLanguage = (lang) => {
  const t = translations[lang] || translations['pt-BR'];

  document.documentElement.lang = lang;
  document.title = t.title;

  const metaDescription = document.querySelector('meta[name="description"]');
  if (metaDescription) metaDescription.setAttribute('content', t.description);

  const switcher = document.querySelector('.language-switcher');
  if (switcher) switcher.setAttribute('aria-label', t.langLabel);

  setTexts('nav a', t.nav);
  setText('.badge', t.heroBadge);
  setText('.hero h2', t.heroTitle);
  setText('.hero p', t.heroText);
  setText('.hero-buttons .primary', t.explore);
  setText('.discord-hero', t.joinDiscord, 0);
  setText('.discord-hero', t.joinDiscord, 1);
  setText('.support-btn', t.supportButton);
  setText('.hero-buttons .support-hero', t.supportCoffee);
  setText('.community-actions .support-hero', t.supportProject);
  setText('.footer-support-link', t.supportButton.replace('☕ ', ''));
  setTexts('.stats span', t.stats);
  setText('.terminal pre', t.terminal);

  document.querySelectorAll('.card').forEach((card, index) => {
    if (!t.features[index]) return;
    const [title, text] = t.features[index];
    const h3 = card.querySelector('h3');
    const p = card.querySelector('p');
    if (h3) h3.textContent = title;
    if (p) p.textContent = text;
  });

  setText('#fixes .section-title h2', t.fixesTitle);
  setText('#fixes .section-title p', t.fixesSubtitle);
  document.querySelectorAll('.repo-card').forEach((card, index) => {
    const status = card.querySelector('.repo-top span');
    const desc = card.querySelector('p');
    if (status) status.textContent = index === 0 ? t.stable : t.testing;
    if (desc && t.repoDescriptions[index]) desc.textContent = t.repoDescriptions[index];
  });

  document.querySelectorAll('.download-link').forEach((link) => {
    link.innerHTML = `<span class="download-icon">⬇</span><span>${t.downloadFix}</span>`;
  });

  document.querySelectorAll('.repo-link').forEach((link) => {
    link.textContent = t.viewProject;
  });

  setText('#games .section-title h2', t.gamesTitle);
  setText('#games .section-title p', t.gamesSubtitle);
  document.querySelectorAll('.game-card').forEach((card, index) => {
    const status = card.querySelector('.game-status');
    const desc = card.querySelector('.game-info p');
    if (status) status.textContent = index === 0 ? t.stable : t.testing;
    if (desc && t.gameDescriptions[index]) desc.textContent = t.gameDescriptions[index];
  });

  setText('.community-box h2', t.communityTitle);
  setText('.community-box p', t.communityText);
  setText('.community-actions .primary', t.githubView);
  setText('.community-actions .discord-hero', t.joinDiscord);

  setText('#faq .section-title h2', t.faqTitle);
  document.querySelectorAll('.faq-item').forEach((item, index) => {
    if (!t.faq[index]) return;
    const [question, answer] = t.faq[index];
    const h4 = item.querySelector('h4');
    const p = item.querySelector('p');
    if (h4) h4.textContent = question;
    if (p) p.textContent = answer;
  });

  setText('footer .footer > div:first-child p', t.footer);

  document.querySelectorAll('.lang-btn').forEach((button) => {
    button.classList.toggle('active', button.dataset.lang === lang);
  });

  localStorage.setItem('reviva-lang', lang);
};

const cards = document.querySelectorAll('.repo-card, .card, .faq-item, .game-card');

const revealOnScroll = () => {
  cards.forEach((card) => {
    const top = card.getBoundingClientRect().top;
    const visiblePoint = window.innerHeight - 80;

    if (top < visiblePoint) {
      card.classList.add('visible');
    }
  });
};

document.querySelectorAll('a[href^="#"]').forEach((link) => {
  link.addEventListener('click', (event) => {
    const target = document.querySelector(link.getAttribute('href'));

    if (!target) return;
    event.preventDefault();
    target.scrollIntoView({ behavior: 'smooth', block: 'start' });
  });
});

document.querySelectorAll('.faq-item h4').forEach((title) => {
  title.addEventListener('click', () => {
    title.parentElement.classList.toggle('open');
  });
});

document.querySelectorAll('.lang-btn').forEach((button) => {
  button.addEventListener('click', () => setLanguage(button.dataset.lang));
});

const savedLanguage = localStorage.getItem('reviva-lang') || 'pt-BR';
setLanguage(savedLanguage);
window.addEventListener('scroll', revealOnScroll);
window.addEventListener('load', revealOnScroll);
